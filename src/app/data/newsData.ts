export function getNewsById(id: number): NewsItem | undefined {
    return newsData.find(news => news.id === id);
}

// 辅助函数：获取新闻列表（仅包含列表页需要的字段）
export function getNewsList() {
    return newsData.map(({ id, title, summary, date, category, image }) => ({
        id,
        title,
        summary,
        date,
        category,
        image,
    }));
}
